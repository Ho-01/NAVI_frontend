/* 경로/장소별 tips 합치기 */
export function getTipsFor({ from, to }) {
  const f = norm(from);
  const t = norm(to);
  const byRoute = TipsDB.routes?.[`${f}>${t}`] ?? [];
  const byPlace = TipsDB.places?.[t] ?? [];
  const common = TipsDB.common?.COMMON ?? [];
  // 중복 제거
  return [...new Set([...byRoute, ...byPlace, ...common].filter(Boolean))];
}
